class SplashScreen {
    constructor(callback) {
        this.callback = callback;
        this.dirty = true;
    }

    start() {
        setTimeout(() => {
            this.draw();  
            if (this.callback) {
                this.callback();  
            }
        }, 2000);  
    }

    draw() {
        if (this.dirty) {
            this.dirty = false;
            console.log(ANSI.CLEAR_SCREEN, ANSI.CURSOR_HOME);  
            console.log(outputGraphics);  
        }
    }
}

export default SplashScreen;
